package Pojo_Post;

public class RootPut {
	
	
	    private String description;
	    private int id;
	    private String name;
		
	    public String getDescription() {
			return description;
		}

		public int getId() {
			return id;
		}

		public String getName() {
			return name;
		}

		public RootPut(String description, int id, String name) {
			super();
			this.description = description;
			this.id = id;
			this.name = name;
		}
	

}
